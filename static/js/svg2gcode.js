/**
 * SVG to GCode converter
 * Supports: path (M,L,H,V,C,Q,A,Z), rect, circle, ellipse, line, polyline, polygon
 */
class SVGtoGCode {
  constructor(options = {}) {
    this.opts = {
      feedRate:     options.feedRate    ?? 800,
      seekRate:     options.seekRate    ?? 2000,
      zSafe:        options.zSafe       ?? 5,
      zCut:         options.zCut        ?? 0,
      scale:        options.scale       ?? 1,
      originX:      options.originX     ?? 0,
      originY:      options.originY     ?? 0,
      flipY:        options.flipY       ?? true,
      passes:       options.passes      ?? 1,
      passDepth:    options.passDepth   ?? 0,
      laserMode:    options.laserMode   ?? false,
      spindleSpeed: options.spindleSpeed ?? 10000,
      docHeight:    0,
    };
  }

  convert(svgText) {
    const parser = new DOMParser();
    const doc    = parser.parseFromString(svgText, 'image/svg+xml');
    const svg    = doc.querySelector('svg');
    if (!svg) throw new Error('Invalid SVG');

    // Determine document height for Y flip
    const vb = svg.getAttribute('viewBox');
    if (vb) {
      const parts = vb.split(/[\s,]+/).map(Number);
      this.opts.docHeight = parts[3] || parseFloat(svg.getAttribute('height')) || 100;
    } else {
      this.opts.docHeight = parseFloat(svg.getAttribute('height')) || 100;
    }

    const lines = [];
    lines.push('; GCode generated by MiniCNC SVG→GCode converter');
    lines.push(`; Feed: ${this.opts.feedRate}mm/min  Seek: ${this.opts.seekRate}mm/min`);
    lines.push(`; Z safe: ${this.opts.zSafe}mm  Z cut: ${this.opts.zCut}mm`);
    lines.push('');
    lines.push('G90 G21 ; absolute, millimeters');
    if (!this.opts.laserMode) {
      lines.push(`M3 S${this.opts.spindleSpeed} ; spindle on`);
    }
    lines.push(`G0 Z${this.opts.zSafe} ; lift`);
    lines.push('G0 X0 Y0 ; home');
    lines.push('');

    const shapes = this._extractShapes(svg);
    for (const pts of shapes) {
      if (pts.length < 2) continue;
      lines.push(...this._shapeToGCode(pts));
    }

    lines.push('');
    lines.push(`G0 Z${this.opts.zSafe}`);
    lines.push('G0 X0 Y0');
    if (!this.opts.laserMode) {
      lines.push('M5 ; spindle off');
    }
    lines.push('M2 ; end');
    return lines.join('\n');
  }

  _extractShapes(root) {
    const shapes = [];
    const els = root.querySelectorAll('path,rect,circle,ellipse,line,polyline,polygon');

    els.forEach(el => {
      const tag = el.tagName.toLowerCase();
      let pts = [];
      try {
        if (tag === 'path')     pts = this._parsePath(el);
        else if (tag === 'rect')     pts = this._parseRect(el);
        else if (tag === 'circle')   pts = this._parseCircle(el);
        else if (tag === 'ellipse')  pts = this._parseEllipse(el);
        else if (tag === 'line')     pts = this._parseLine(el);
        else if (tag === 'polyline' || tag === 'polygon') pts = this._parsePolyline(el, tag === 'polygon');
      } catch(e) { console.warn('Shape parse error:', e); }

      if (pts.length) {
        // Apply transform from element's CTM if available
        shapes.push(pts.map(p => this._transform(p)));
      }
    });
    return shapes;
  }

  _transform({ x, y, pen }) {
    let tx = x * this.opts.scale + this.opts.originX;
    let ty = this.opts.flipY
      ? (this.opts.docHeight - y) * this.opts.scale + this.opts.originY
      : y * this.opts.scale + this.opts.originY;
    return { x: tx, y: ty, pen };
  }

  _shapeToGCode(pts) {
    const lines = [];
    const { feedRate, seekRate, zSafe, zCut, passes, passDepth, laserMode } = this.opts;

    for (let pass = 0; pass < passes; pass++) {
      const zDepth = zCut - pass * passDepth;

      pts.forEach(({ x, y, pen }, i) => {
        const fx = x.toFixed(3);
        const fy = y.toFixed(3);

        if (pen === 'up' || i === 0) {
          if (laserMode) {
            lines.push(`G0 X${fx} Y${fy}`);
          } else {
            lines.push(`G0 Z${zSafe}`);
            lines.push(`G0 X${fx} Y${fy}`);
            lines.push(`G1 Z${zDepth} F${Math.round(feedRate * 0.4)}`);
          }
          if (laserMode) lines.push(`M4 S${this.opts.spindleSpeed}`);
        } else {
          lines.push(`G1 X${fx} Y${fy} F${feedRate}`);
        }
      });

      if (!laserMode) lines.push(`G0 Z${zSafe}`);
      else            lines.push('M5');
    }
    lines.push('');
    return lines;
  }

  /* ── Shape parsers ────────────────────────────────────────────────────────── */

  _parseLine(el) {
    return [
      { x: +el.getAttribute('x1')||0, y: +el.getAttribute('y1')||0, pen: 'up'   },
      { x: +el.getAttribute('x2')||0, y: +el.getAttribute('y2')||0, pen: 'down' },
    ];
  }

  _parseRect(el) {
    const x  = +el.getAttribute('x') || 0;
    const y  = +el.getAttribute('y') || 0;
    const w  = +el.getAttribute('width')  || 0;
    const h  = +el.getAttribute('height') || 0;
    const rx = +el.getAttribute('rx') || 0;
    if (!w || !h) return [];
    // Simplified (no corner radius drawn)
    return [
      { x, y, pen: 'up' },
      { x: x+w, y, pen: 'down' },
      { x: x+w, y: y+h, pen: 'down' },
      { x, y: y+h, pen: 'down' },
      { x, y, pen: 'down' },
    ];
  }

  _parseCircle(el) {
    const cx = +el.getAttribute('cx') || 0;
    const cy = +el.getAttribute('cy') || 0;
    const r  = +el.getAttribute('r')  || 0;
    return this._circlePoints(cx, cy, r, r);
  }

  _parseEllipse(el) {
    const cx = +el.getAttribute('cx') || 0;
    const cy = +el.getAttribute('cy') || 0;
    const rx = +el.getAttribute('rx') || 0;
    const ry = +el.getAttribute('ry') || 0;
    return this._circlePoints(cx, cy, rx, ry);
  }

  _circlePoints(cx, cy, rx, ry, segments = 48) {
    const pts = [];
    for (let i = 0; i <= segments; i++) {
      const a = (i / segments) * Math.PI * 2;
      pts.push({ x: cx + Math.cos(a)*rx, y: cy + Math.sin(a)*ry, pen: i === 0 ? 'up' : 'down' });
    }
    return pts;
  }

  _parsePolyline(el, close) {
    const raw = el.getAttribute('points') || '';
    const nums = raw.trim().split(/[\s,]+/).map(Number);
    const pts  = [];
    for (let i = 0; i < nums.length - 1; i += 2) {
      pts.push({ x: nums[i], y: nums[i+1], pen: i === 0 ? 'up' : 'down' });
    }
    if (close && pts.length > 2) {
      pts.push({ ...pts[0], pen: 'down' });
    }
    return pts;
  }

  _parsePath(el) {
    const d = el.getAttribute('d') || '';
    const pts = [];
    let x = 0, y = 0, sx = 0, sy = 0;

    const tokens = d.match(/[MmLlHhVvCcQqAaZz]|[+-]?[0-9]*\.?[0-9]+(?:e[+-]?[0-9]+)?/gi) || [];
    let i = 0;
    let cmd = 'M';

    const num  = () => parseFloat(tokens[i++]);
    const rel  = () => cmd === cmd.toLowerCase();

    while (i < tokens.length) {
      const t = tokens[i];
      if (/[MmLlHhVvCcQqAaZz]/i.test(t)) { cmd = t; i++; }

      const upper = cmd.toUpperCase();

      if (upper === 'M') {
        const nx = rel() ? x + num() : num();
        const ny = rel() ? y + num() : num();
        x = nx; y = ny; sx = x; sy = y;
        pts.push({ x, y, pen: 'up' });
        cmd = rel() ? 'l' : 'L';
      } else if (upper === 'L') {
        x = rel() ? x + num() : num();
        y = rel() ? y + num() : num();
        pts.push({ x, y, pen: 'down' });
      } else if (upper === 'H') {
        x = rel() ? x + num() : num();
        pts.push({ x, y, pen: 'down' });
      } else if (upper === 'V') {
        y = rel() ? y + num() : num();
        pts.push({ x, y, pen: 'down' });
      } else if (upper === 'Z') {
        pts.push({ x: sx, y: sy, pen: 'down' });
        x = sx; y = sy;
      } else if (upper === 'C') {
        const x1 = rel() ? x + num() : num();
        const y1 = rel() ? y + num() : num();
        const x2 = rel() ? x + num() : num();
        const y2 = rel() ? y + num() : num();
        const ex = rel() ? x + num() : num();
        const ey = rel() ? y + num() : num();
        const bezier = this._cubicBezier(x, y, x1, y1, x2, y2, ex, ey);
        bezier.forEach(p => pts.push({ ...p, pen: 'down' }));
        x = ex; y = ey;
      } else if (upper === 'Q') {
        const x1 = rel() ? x + num() : num();
        const y1 = rel() ? y + num() : num();
        const ex = rel() ? x + num() : num();
        const ey = rel() ? y + num() : num();
        const bezier = this._quadBezier(x, y, x1, y1, ex, ey);
        bezier.forEach(p => pts.push({ ...p, pen: 'down' }));
        x = ex; y = ey;
      } else if (upper === 'A') {
        const rx = num(), ry = num(), xrot = num(), laf = num(), sf = num();
        const ex = rel() ? x + num() : num();
        const ey = rel() ? y + num() : num();
        const arc = this._arcToCurve(x, y, rx, ry, xrot, laf, sf, ex, ey);
        arc.forEach(p => pts.push({ ...p, pen: 'down' }));
        x = ex; y = ey;
      } else {
        i++; // skip unknown
      }
    }

    return pts;
  }

  _cubicBezier(x0, y0, x1, y1, x2, y2, x3, y3, steps = 20) {
    const pts = [];
    for (let t = 1/steps; t <= 1+1e-9; t += 1/steps) {
      const u = 1-t;
      pts.push({
        x: u**3*x0 + 3*u**2*t*x1 + 3*u*t**2*x2 + t**3*x3,
        y: u**3*y0 + 3*u**2*t*y1 + 3*u*t**2*y2 + t**3*y3,
      });
    }
    return pts;
  }

  _quadBezier(x0, y0, x1, y1, x2, y2, steps = 16) {
    const pts = [];
    for (let t = 1/steps; t <= 1+1e-9; t += 1/steps) {
      const u = 1-t;
      pts.push({
        x: u**2*x0 + 2*u*t*x1 + t**2*x2,
        y: u**2*y0 + 2*u*t*y1 + t**2*y2,
      });
    }
    return pts;
  }

  _arcToCurve(x1, y1, rx, ry, phi, fA, fS, x2, y2) {
    // Simplified: approximate with line segments
    const pts = [];
    const dx = (x1 - x2) / 2;
    const dy = (y1 - y2) / 2;
    const cx = (x1 + x2) / 2;
    const cy = (y1 + y2) / 2;
    const r  = Math.max(Math.abs(rx), Math.abs(ry), 1);
    const steps = 16;
    for (let i = 1; i <= steps; i++) {
      const t = i / steps;
      pts.push({
        x: cx + r * Math.cos(Math.atan2(dy, dx) + (fS ? 1 : -1) * Math.PI * t),
        y: cy + r * Math.sin(Math.atan2(dy, dx) + (fS ? 1 : -1) * Math.PI * t),
      });
    }
    return pts;
  }
}
